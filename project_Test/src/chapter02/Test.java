package chapter02;

public class Test {
	
	public static void main(String[] args) {
		
		int num = 10;
		int num2 = 20; // 변수 num 에 20을 할당 합니다.

		
		/*
		 * 여러 라인 주석 처리 입니다. 
		 * 주석은 프로그램에서 해석되지 않는 부분 입니다.
		 * 블록 잡고 ctrl + = 블록 주석
		 * 블록잡고 ctrl + shift + \  = 블록 주석 취소
		 * 해당 라인에서 ctrl + /  = 한 라인 주석처리 
		 */
		 
		 
		
		System.out.println( num );	 // num을 출력
		System.out.println( num2 );  // num2를 출력
		System.out.println( num+ num2);  // num + num2를 출력
 
		
//		이 곳은 주석으로 출력되지 않습니다. ctrl + / 한 라인 주석
		
		
	}

}
