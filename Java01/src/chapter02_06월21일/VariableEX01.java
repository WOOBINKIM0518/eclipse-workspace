package chapter02_06월21일;

public class VariableEX01 {

	public static void main(String[] args) {

		int lebel;  // 변수를 선언 후 값을 할당 
		lebel = 10;
		lebel = 50;
		lebel = 1000;
		
		int lebel2 = 20;   // 변수 선언과 동시에 값을 할당
		lebel2 = 100;
		lebel2 = 2000;         // 마지막 변수로 출력됨 
		
		System.out.println(lebel);
		System.out.println(lebel2);
		
	}

}
