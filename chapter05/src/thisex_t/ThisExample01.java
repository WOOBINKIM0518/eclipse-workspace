package thisex_t;


	
	class BirthDay {
		
		int day;
		int month;
		int year;
		
		public void setYear(int year) {
			this.year =year;
		}

		public void printThis() {
			System.out.println(this);		//this가 가르키는 객체의 힙 메모리 주소
		}

		
		
	public static void main(String[] arg) {
		BirthDay bDay = new BirthDay();
		bDay.setYear(2000);
		System.out.println(bDay);		//bDay 객체의 RAM의 힙주소 출력(JVM이만들어놓은주소)) ==>thisex.BirthDay@123a439b
		bDay.printThis();				//bDay가 가르키는 힙메모리 주소  ==>thisex.BirthDay@123a439b
		
		System.out.println(bDay.year);

	}

}

